class Solution {
public:
    vector<vector<int>> threeSum(vector<int>& nums) {
        if (nums.size() < 3)
        {
            return vector<vector<int>>();
        }

        unordered_map<int, int> mapNum2Count;

        set<vector<int>> vecResult;

        size_t length = nums.size();
        for (size_t a = 0; a < length; a++)
        {
            int num = nums[a];
            mapNum2Count[num]++;
        }

        if (mapNum2Count.size() == 1)
        {
            auto begin = mapNum2Count.begin();
            if (begin->first == 0)
            {
                return { {0, 0, 0} };
            }
        }

        for (auto iter = mapNum2Count.begin(); iter != mapNum2Count.end(); iter++)
        {
            for (auto iter2 = mapNum2Count.begin(); iter2 != mapNum2Count.end(); iter2++)
            {
                int num_a = iter->first;
                int num_b = iter2->first;

                int num_c = -(num_a + num_b);

                if (mapNum2Count.find(num_c) != mapNum2Count.end())
                {
                    mapNum2Count[num_a]--;
                    mapNum2Count[num_b]--;
                    mapNum2Count[num_c]--;

                    if (mapNum2Count[num_a] >= 0
                        && mapNum2Count[num_b] >= 0
                        && mapNum2Count[num_c] >= 0)
                    {
                        vector<int> vecRe;
                        vecRe.push_back(num_a);
                        vecRe.push_back(num_b);
                        vecRe.push_back(num_c);
                                    
                        sort(vecRe.begin(), vecRe.end());

                        vecResult.insert(vecRe);
                    }

                    mapNum2Count[num_a]++;
                    mapNum2Count[num_b]++;
                    mapNum2Count[num_c]++;
                }
            }
        }

        vector<vector<int>> vecReturn;
        for (auto vec : vecResult)
        {
            vecReturn.push_back(vec);
        }

        return vecReturn;
    }
};

